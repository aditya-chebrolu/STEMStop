"""# Loops

Loops allow a set of actions to be performed to every element in a list in an efficient manner. To loop through an entire list, we can use a "for" loop. Its implementation is shown below:
"""

scientists = ["einstein", "hawking", "newton", "feynman", "faraday"]
for scientist in scientists:
  print(scientist)

"""As you can see, the for loop "traverses" across each element of the list and stores this element in the variable "scientist". We can then print out all the elements in the "scientists" list.


However, simply printing each element is the only operation that can be performed inside a for-loop. Any operation relative to a data type can be performed for an element and can be placed inside a for-loop. An example of this is shown below:
"""

tests = ["sat", "act"]
for test in tests:
  print("I do not want to take the " + test.upper() + " this august.")

"""## Looping with Numbers

To generate a series of numbers, we can use the "range()" function. The range function simply returns an array containing a set of numbers between 2 numbers passed as arguments to the function. Its implementation is shown below with a for-loop:
"""

for number in range(0, 7):
  print(number)

"""As you can see, if we pass the integers m and n as arguments to the range() function as range(m, n), the function returns the range of values between m and n - 1. 

We can also use the range() function to return a set of numbers that are incremented by an integer more than one, meaning that they can skip numbers in a given range. We can do this simply by passing a third argument to the range() function, as shown in the code implementation below to print all odd numbers from 0 to 10:
"""

for odd_number in range(1, 10, 2):
  print(odd_number)

