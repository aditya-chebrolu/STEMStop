"""Individual elements can be accessed in a list simply by the name of the list followed by the "index" of the desired element. An index is simply the position of a certain element in a list. It is important to note, however, that in Python (and in most other programming languages), the indexing of lists is zero-based, meaning that the first element has an index of 0, not 1, the second element has an index of 1, not 2, and so on. 

An example of accessing an element can be shown here:
"""

my_favorite_foods = ["ice cream", "pizza", "chocolate chip cookies"]
