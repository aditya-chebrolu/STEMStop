"""An interesting aspect of accessing elements in Python is that indexes can be negative. For example, the index -1 returns the last element in a list.

It is important to note that lists are "dynamic" in Python, meaning that elements can be added, changed, inserted, or removed. Each of these three operations will be looked at below:

1. **Adding Elements:** When elements are added, or "appended" to the list, they are added to the end of the list. This can be done using the append() method:
"""

